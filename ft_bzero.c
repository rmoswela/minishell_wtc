#include "minishell.h"

void			ft_bzero(char *s, size_t n)
{
	size_t		i;
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	i = 0;
	while (i < n)
		ptr[i++] = '\0';
	ptr[i] = '\0';
}
